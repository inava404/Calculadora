    private void botonPuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPuntoActionPerformed
        if(punto == true){
            if(cadenaNums.equals("")){ //Si esta vacio, se añade a la cadena el 0.
                cadenaNums = "0.";
            }else{
                cadenaNums += "."; //Si tiene un valor, se añade el punto a la cadena
            }
            etiquetaNums.setText(cadenaNums);
            punto = false;
        }
    }//GEN-LAST:event_botonPuntoActionPerformed

    private void botonPosNegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPosNegActionPerformed
        if(cadenaNums.charAt(0) != '-'){ //Si la primer letra de la cadena no tiene un menos, se le agrega el menos, haciendo el numero negativo
            cadenaNums = "-" + cadenaNums;
        }else{ //Caso contrario, al tener un signo de menos, se le quita el primer elemento de la cadena, o sea el signo de menos, gracias al substring
            cadenaNums = cadenaNums.substring(1, cadenaNums.length()); 
        }
        etiquetaNums.setText(cadenaNums);
    }//GEN-LAST:event_botonPosNegActionPerformed

