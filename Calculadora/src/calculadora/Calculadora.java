    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
        double segundoNum;
        if(operacion.equals("nula")){
            etiquetaNums.setText(cadenaNums);
        }
        
        else if(operacion.equals("sumar")){
            segundoNum = Double.parseDouble(cadenaNums);
            resultado = primerNum + segundoNum;
            etiquetaNums.setText(String.format("% .2f", resultado));
            cadenaNums = String.valueOf(resultado); //Convirtiendo Double a String
            operacion = "nula";
        }
        
        else if(operacion.equals("restar")){
            segundoNum = Double.parseDouble(cadenaNums);
            resultado = primerNum - segundoNum;
            etiquetaNums.setText(String.format("% .2f", resultado));
            cadenaNums = String.valueOf(resultado); //Convirtiendo Double a String
            operacion = "nula";
        }
        
        else if(operacion.equals("multiplicar")){
            segundoNum = Double.parseDouble(cadenaNums);
            resultado = primerNum * segundoNum;
            etiquetaNums.setText(String.format("% .2f", resultado));
            cadenaNums = String.valueOf(resultado); //Convirtiendo Double a String
            operacion = "nula";
        }
        
        else if(operacion.equals("dividir")){
            segundoNum = Double.parseDouble(cadenaNums);
            if(segundoNum == 0){
                etiquetaNums.setText("No es posible dividir entre 0");
            }else{
                resultado = primerNum / segundoNum;
                etiquetaNums.setText(String.format("% .2f", resultado));
                cadenaNums = String.valueOf(resultado); //Convirtiendo Double a String
                operacion = "nula";
            }
        }
        
        else if(operacion.equals("porcentaje")){
            segundoNum = Double.parseDouble(cadenaNums);
            resultado = (primerNum * segundoNum)/100;
            etiquetaNums.setText(String.format("% .2f", resultado));
            cadenaNums = String.valueOf(resultado); //Convirtiendo Double a String
            operacion = "nula";
        }
        
        
        etiquetaMuestra.setText("");
        activado = true;
        punto = false;
    }//GEN-LAST:event_botonIgualActionPerformed

